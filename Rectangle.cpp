#include<Rectangle.hpp>

// //////////////////////////////////////////////////////////////////////
Rectangle::Rectangle (const double &iWidth, const double &iHeight):
                     m_height(iHeight),
                     m_width(iWidth)
                     {}

// //////////////////////////////////////////////////////////////////////
Rectangle::~Rectangle () {}

// //////////////////////////////////////////////////////////////////////
double Rectangle::area () 
{
    return (m_width * m_height);
}

// //////////////////////////////////////////////////////////////////////
double Rectangle::perimeter () 
{
    return (2 * (m_width + m_height));
}
