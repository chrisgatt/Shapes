#include<Circle.hpp>

// //////////////////////////////////////////////////////////////////////
Circle::Circle (const double &iRadius):
                     m_radius(iRadius)
                     {}

// //////////////////////////////////////////////////////////////////////
Circle::~Circle () {}

// //////////////////////////////////////////////////////////////////////
double Circle::area () 
{
    return (m_pi * m_radius * m_radius);
}

// //////////////////////////////////////////////////////////////////////
double Circle::perimeter () 
{
    return (2 * m_pi * m_radius);
}
